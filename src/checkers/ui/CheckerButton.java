package checkers.ui;

import javax.swing.Action;
import javax.swing.Icon;
import javax.swing.JButton;

public class CheckerButton extends JButton {

	public CheckerButton() {
	}

	public CheckerButton(Icon arg0) {
		super(arg0);
	}

	public CheckerButton(String arg0) {
		super(arg0);
	}

	public CheckerButton(Action arg0) {
		super(arg0);
	}

	public CheckerButton(String arg0, Icon arg1) {
		super(arg0, arg1);
	}

}
